from colorama import *

def green(text):
	text = Fore.GREEN + text + Fore.RESET
	return text

def red(text):
	text = Fore.RED + text + Fore.RESET
	return text

def blue(text):
	text = Fore.BLUE + text + Fore.RESET
	return text

def cyan(text):
	text = Fore.CYAN + text + Fore.RESET
	return text

def yellow(text):
	text = Fore.YELLOW + text + Fore.RESET
	return text

def white(text):
	text = Fore.WHITE + text + Fore.RESET
	return text

def black(text):
	text = Fore.BLACK + text + Fore.RESET
	return text